# 映射类型之字典

# 字典的创建一：直接创建
# 通过键获取值
dict1 = {'1':'赵', '2':'钱', '3':'孙', '4':'李'}
print(dict1['1'])

# 字典的创建二：通过BIF创建
# 由于dict只能接受一个参数，因此需要使用()将序列包装起来
dict2 = dict((['a',65],['b',66],['c',67]))
print(dict2['a'])

# 字典值的修改
dict1['4'] = '彭'
print(dict1['4'])

# 如果所修改的键不在字典中，则会自动增加
dict1['5'] = '周'
print(dict1)
